package Logica;
public class c_catalogo {
		private String codarticulo;
		private String descripcion;
		private String codfam;
		private String stockactual;
		private String pvunt;
		public String getCodarticulo() {
			return codarticulo;
		}
		public void setCodarticulo(String codarticulo) {
			this.codarticulo = codarticulo;
		}
		public String getDescripcion() {
			return descripcion;
		}
		public void setDescripcion(String descripcion) {
			this.descripcion = descripcion;
		}
		public String getStockactual() {
			return stockactual;
		}
		public void setStockactual(String stockactual) {
			this.stockactual = stockactual;
		}
		public String getPvunt() {
			return pvunt;
		}
		public void setPvunt(String pvunt) {
			this.pvunt = pvunt;
		}
		public String getCodfam() {
			return codfam;
		}
		public void setCodfam(String codfam) {
			this.codfam = codfam;
		}	
}
