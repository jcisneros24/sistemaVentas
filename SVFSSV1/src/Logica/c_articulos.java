package Logica;
public class c_articulos {
		private int idarticulo;
		private String codarticulo;
		private String descripcion;
		private String color;
		private String codfam;
		private String codmedida;
		private String fingreso;
		private String codprove;
		private String estado;
		
		public String getCodarticulo() {
			return codarticulo;
		}
		public void setCodarticulo(String codarticulo) {
			this.codarticulo = codarticulo;
		}
		public String getDescripcion() {
			return descripcion;
		}
		public void setDescripcion(String descripcion) {
			this.descripcion = descripcion;
		}
		public String getCodfam() {
			return codfam;
		}
		public void setCodfam(String codfam) {
			this.codfam = codfam;
		}
		public String getCodmedida() {
			return codmedida;
		}
		public void setCodmedida(String codmedida) {
			this.codmedida = codmedida;
		}
		public String getFingreso() {
			return fingreso;
		}
		public void setFingreso(String fingreso) {
			this.fingreso = fingreso;
		}
		public String getCodprove() {
			return codprove;
		}
		public void setCodprove(String codprove) {
			this.codprove = codprove;
		}
		public int getIdarticulo() {
			return idarticulo;
		}
		public void setIdarticulo(int idarticulo) {
			this.idarticulo = idarticulo;
		}
		public String getColor() {
			return color;
		}
		public void setColor(String color) {
			this.color = color;
		}
		public String getEstado() {
			return estado;
		}
		public void setEstado(String estado) {
			this.estado = estado;
		}
				
}
