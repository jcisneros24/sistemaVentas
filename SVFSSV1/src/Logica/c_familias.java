package Logica;
public class c_familias {
  private String hija;
  private String madre;
  private String codfam;
  private int idfam;
  
    public String getHija() {
        return hija;
    }
    public String getCodfam() {
        return codfam;
    }
    public void setCodfam(String codfam) {
        this.codfam = codfam;
    }
    public void setHija(String hija) {
        this.hija = hija;
    }
    public String getMadre() {
        return madre;
    }
    public void setMadre(String madre) {
        this.madre = madre;
    }
	public int getIdfam() {
		return idfam;
	}
	public void setIdfam(int idfam) {
		this.idfam = idfam;
	}
    
}
